import Data.Char (ord)

-- PART 1 SOLUTION
decode1 :: String -> (Integer,Integer)
decode1 dir =
    let
        [direction, numStr] = words dir
        num = read numStr :: Integer
    in
        case direction of
            "forward" -> (0,num)
            "down" -> (num,0)
            "up" -> (-num,0)
            _ -> (0,0)

part1 :: [String] -> Integer
part1 dirList =
    let
        xs = map decode1 dirList
        (dpth,hor) = foldl (\(accD,accH) (d,h) -> (accD+d,accH+h)) (0,0) xs
    in
        dpth * hor

-- PART 2 SOLUTION

foldFn :: [Integer] -> String -> [Integer]
foldFn acc cur =
    let
        [d,h,a] = acc
        [direction, numStr] = words cur
        num = read numStr :: Integer
    in
        case direction of
            "forward" -> [d+(a*num),h+num,a]
            "down" -> [d,h,a+num]
            "up" -> [d,h,a-num]
            _ -> [d,h,a]


part2 :: [String] -> Integer
part2 dirList =
    let
        [dpth, horiz, _] = foldl foldFn [0,0,0] dirList
    in
        dpth * horiz


